int		ft_wc(char const *s, char c)
{
	unsigned int wc;

	wc = 0;
	while (*s != '\0')
	{
		while (*s != '\0' && *s == c)
			s++;
		if (*s != '\0')
			wc++;
		while (*s != '\0' && *s != c)
			s++;
	}
	return (wc);
}
